	def avanzar(self):
		""" Avanzar: Función perteneciente a la clase coche, la cual me imprime en pantalla el sonido que emite un vehículo al circular"""
		print('Runnn Runnn')
	def claxon(self):
		""" claxon: Función perteneciente a coche, la cual imprime en pantalla el sonido que emite el claxon de un vehículo """
